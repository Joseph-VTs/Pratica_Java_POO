/*
    - Objetos: Representam Elementos do Mundo Real,
    Mais Também Problemas Relacionados a Eles.

    - Classes: Representa uma Série de Objetos que têm as mesmas Características

    - Os Atributos e Métodos são combinados para definir as ações de um Objeto no Sistema.

    Ex:
    Atributos (Proprietário, Marca, Ano, Placa),
    São Caracteristicas do Objeto

    Métodos (Vender, Registrar, Transferir_Proprietário, Mudar_Placa),
    Ações ou Comportamento que seus Objetos terão


    - Diferenças entre os métodos
    Os Métodos de Acesso são aqueles que fornecem informações sobre o Objeto (get),

    Os Métodos Modificadores (set) são aqueles que modificam o estado de um Objeto (Valor de um ou mais campos)

*/